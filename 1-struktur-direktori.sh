STRUKTUR DIREKTORI

/      -> Root direktori dari seluruh file-system hierarchy
/bin   -> File biner yg berisi perintah-perintah linux
/boot  -> Boot Loaders file, kernels, dll
/dev   -> Mewakili hardware sistem, harddrive, keyboard, dll
/etc   -> File konfigurasi seluruh sistem
/home  -> User non-root home direktori, berisi file, personal setting tiap user
/lib   -> Library yang digunakan program-program sistem
/media -> Titik mount media yg diakses sementara seperti usb & cd-rom
/mnt   -> Titik mount sistem berkas tambahan/ media yg diakses manual
/opt   -> Paket software selain distribusi inti os
/proc  -> Informasi proses dan status sistem
/root  -> User root home direktori
/run   -> Data yg berubah saat runtime, seperti id proses dan socket file
/sbin  -> Perintah administratif superuser
/srv   -> Data sistem sebagai layanan untuk pengguna
/sys   -> Antarmuka struktur internal dari kernel linux
/tmp   -> File sementara, tidak disimpan diantara reboot system
/usr   -> Secondary hierarchy untuk read-only user data
/var   -> Variable data yang berisi konfigurasi system, berisi file, database, log, dll

